CREATE TEMP TABLE _xl_matched AS
SELECT x.*, c.id AS crm_id,
  CASE
    WHEN c.legacy_id IS NOT NULL AND c.legacy_id = x.excel_id THEN 'legacy_id'
    WHEN x.birth_no <> '' AND regexp_replace(coalesce(c.birth_number,''), '\D', '', 'g') = x.birth_no THEN 'birth_number'
    WHEN x.email <> '' AND lower(trim(coalesce(c.email,''))) = x.email THEN 'email'
    WHEN x.mobile9 <> '' AND right(regexp_replace(coalesce(c.mobile,''), '\D', '', 'g'), 9) = x.mobile9 THEN 'mobile'
    ELSE 'name'
  END AS matched_by
FROM _xl_collab x
JOIN LATERAL (
  SELECT * FROM collaborators c
  WHERE c.legacy_id = x.excel_id
     OR (x.birth_no <> '' AND regexp_replace(coalesce(c.birth_number,''), '\D', '', 'g') = x.birth_no)
     OR (x.email <> '' AND lower(trim(coalesce(c.email,''))) = x.email)
     OR (x.mobile9 <> '' AND right(regexp_replace(coalesce(c.mobile,''), '\D', '', 'g'), 9) = x.mobile9)
     OR (lower(trim(c.first_name)) = lower(x.first_name) AND lower(trim(c.last_name)) = lower(x.last_name))
  LIMIT 1
) c ON true;

\echo '=== SUMMARY ==='
SELECT (SELECT count(*) FROM _xl_collab) AS excel_total,
       (SELECT count(*) FROM _xl_matched) AS found_in_prod,
       (SELECT count(*) FROM _xl_collab) - (SELECT count(*) FROM _xl_matched) AS missing;

\echo '=== MATCHED BY (kvalita zhody) ==='
SELECT matched_by, count(*) FROM _xl_matched GROUP BY 1 ORDER BY 2 DESC;

\echo '=== MISSING IN PRODUCTION (nie su v INDEXUSe) ==='
SELECT x.excel_id, x.company, x.agreement, x.first_name, x.last_name, x.email, x.mobile9
FROM _xl_collab x
WHERE NOT EXISTS (SELECT 1 FROM _xl_matched m WHERE m.excel_id = x.excel_id AND m.agreement = x.agreement)
ORDER BY x.company, x.last_name;
