export type TemplateAutomation = {
  triggerId: string;
  label: string;
  labelL10n?: Record<string, string>;
  description: string;
  actionType: string;
  targetRole: string | null;
  taskDescription: string | null;
  taskDescriptionL10n?: Record<string, string>;
  taskDeadlineOffset: string | null;
  taskPriority: string;
};

export type TemplateStep = {
  stepId: string;
  parentStepId?: string;
  label: string;
  labelL10n?: Record<string, string>;
  description: string;
  descriptionL10n?: Record<string, string>;
  confirmationType: "checkbox" | "radio" | "info";
  role: string;
  conditionIf: string;
  actionThen: string;
  nextStepId: string;
  callbackTiming: string;
  restrictions?: string;
  isHidden?: boolean;
  automations: TemplateAutomation[];
};

export function getStepLabel(step: TemplateStep, locale: string): string {
  return step.labelL10n?.[locale] ?? step.labelL10n?.["sk"] ?? step.label;
}

export function getAutoLabel(auto: TemplateAutomation, locale: string): string {
  return auto.labelL10n?.[locale] ?? auto.labelL10n?.["sk"] ?? auto.label;
}

export function getStepDescription(step: TemplateStep, locale: string): string {
  return step.descriptionL10n?.[locale] ?? step.descriptionL10n?.["sk"] ?? step.description;
}

export function getAutoTaskDescription(auto: TemplateAutomation, locale: string): string {
  return auto.taskDescriptionL10n?.[locale] ?? auto.taskDescriptionL10n?.["sk"] ?? auto.taskDescription ?? "";
}

